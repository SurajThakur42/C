#include<stdio.h>
 int main(){
    int a = 1;            //integer datatype  -  %d
    float b = 14.54;        //float datatype    -  %f
    char c = 'suraj';      //String or Character datatype   -  %c
    printf("The value of a is %d",a);
    printf("The value of b is %f",b);
    printf("The value of c is %c",c);
    return 0;


 }
